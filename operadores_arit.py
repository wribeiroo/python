# operação com variáveis
x = 5
y = 3
print(x+y)

# outras operações
print(x//y) # divisão inteira
print(x%y) # resto da divisão entre dois n. inteiros
print(x**y) # exponenciação

# operação com strings
text1 = 'Tom '
text2 = 'Ribeiro '
name = text1 + text2
print('')

print(name)
print(name * 3)

# métodos
print(name.upper()) # todas letras maiúsculas
print(name.capitalize()) # primeira letra maiúscula 

